package operators;

public class BitwiseOperator {

	public static void main(String[] args) {
		
//		5	->	101
//		6	->	110
//		4		100

		int a = 10;
		int b = 13;
		
		int c = b << 2;
		
		System.out.println(c);
		
//		10	->	1010
//		13	->	1101
//		15		1111
		
//		1101	->	0110	->	0011	->	0001	->	0000   (RIGHT SHIFT)
		
		
	}

}
